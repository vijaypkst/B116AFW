#print 1 to 10 using while loop
i=1
while i<=10:
    print(i)
    i+=1

print('-'*7)
#print 10 to 1 using while loop
i=10
while i>=1:
    print(i)
    i-=1
print('-' * 7)

#print alternative numbers b/w 1 to 10 using while loop
i=1
while i<=10:
    print(i)
    i+=2
print('-' * 7)
#print even numbers b/w 1 to 10 using while loop
i=1
while i<=10:
    if i%2==0:
        print(i)
    i+=1
print('-' * 7)